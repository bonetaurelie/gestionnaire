<?php

namespace AB\PlatformBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * BookSearch
 *
 * @ORM\Table(name="book_search")
 * @ORM\Entity(repositoryClass="AB\PlatformBundle\Repository\BookSearchRepository")
 */
class BookSearch
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="auteur", type="string", length=255)
     */
    private $auteur;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set auteur
     *
     * @param string $auteur
     * @return BookSearch
     */
    public function setAuteur($auteur)
    {
        $this->auteur = $auteur;

        return $this;
    }

    /**
     * Get auteur
     *
     * @return string 
     */
    public function getAuteur()
    {
        return $this->auteur;
    }
}
